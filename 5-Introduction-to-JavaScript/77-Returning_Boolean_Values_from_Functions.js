/*Returned a boolean value from a function. Equality operations return either
true or false so in the event we create an if/else chain that compares a to b
 that we want to return true or false we can simplify the if/else chain and
 return the comparison instead.  */
 function isLess(a, b) {
  // Fix this code
  return a <= b;
}

// Change these values to test
isLess(6, 19);
