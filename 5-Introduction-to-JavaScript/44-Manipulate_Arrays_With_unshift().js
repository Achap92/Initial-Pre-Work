/*Learned to add elements to an array using the unshift() function. This work like
push() but instead of added the elements to the end, unshift() adds them to them
front.*/
// Example
var ourArray = ["Stimpson", "J", "cat"];
ourArray.shift(); // ourArray now equals ["J", "cat"]
ourArray.unshift("Happy");
// ourArray now equals ["Happy", "J", "cat"]

